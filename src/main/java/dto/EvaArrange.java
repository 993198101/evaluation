package dto;

public class EvaArrange {
    private int cId;
    private String cName;
    private String term;
    private String stuNameAndId;
    private String teaNameAndId;
    private int groupId;
	public int getcId() {
		return cId;
	}
	public void setcId(int cId) {
		this.cId = cId;
	}
	public String getcName() {
		return cName;
	}
	public void setcName(String cName) {
		this.cName = cName;
	}
	public String getTerm() {
		return term;
	}
	public void setTerm(String term) {
		this.term = term;
	}
	public String getStuNameAndId() {
		return stuNameAndId;
	}
	public void setStuNameAndId(String stuNameAndId) {
		this.stuNameAndId = stuNameAndId;
	}
	public String getTeaNameAndId() {
		return teaNameAndId;
	}
	public void setTeaNameAndId(String teaNameAndId) {
		this.teaNameAndId = teaNameAndId;
	}
	public int getGroupId() {
		return groupId;
	}
	public void setGroupId(int groupId) {
		this.groupId = groupId;
	}
    
    
}
